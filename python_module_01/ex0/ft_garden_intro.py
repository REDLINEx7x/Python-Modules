"""Display information about a plant in the garden."""

name = "Rose"
height = "25 cm"
age = "30 days"


def main() -> None:
    """Entry point of the program."""
    print("=== Welcome to My Garden ===")
    print("Plant:", name)
    print("Height:", height)
    print("Age:", age)
    print("\n=== End of Program ===")


if __name__ == "__main__":
    main()
